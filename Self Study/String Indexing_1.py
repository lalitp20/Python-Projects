x = (11, 21, 31, 41, 51, 61, 71, 81, 91)
 
# Positive Indexing
print(x[0])
print(x[3])
print(x[6])
print('=======\n')
 
# Negative Indexing
print(x[-1])
print(x[-5])
print(x[-7])
print('=======\n')
 
# Accessing Nested Tuple Items
Mixed_Tuple = ((1, 2, 3), [4, 5, 6], 'Lalit')
print(Mixed_Tuple[0][0])
print(Mixed_Tuple[1][0])
print(Mixed_Tuple[2][0])
print(Mixed_Tuple[2][4])
